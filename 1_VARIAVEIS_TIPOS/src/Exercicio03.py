# Declaração de variáveis
a = 8
b = 4
c = 2

# ADIÇÃO
soma = a + b + c
print("SOMA:", a, "+", b, "+", c, "=", soma)

# SUBTRAÇÃO
subtracao = a - b - c
print("SUBTRAÇÃO:", a, "-", b, "-", c, "=", subtracao)

# MULTIPLICAÇÃO
multiplicacao = a * b * c
print("MULTIPLICAÇÃO:", a, "*", b, "*", c, "=", multiplicacao)

# DIVISÃO
divisao = a / b / c
print("DIVISÃO:", a, "/", b, "/", c, "=", divisao)
